#include "fillit.h"

int		normalize(char *tetr, int *ar)
{
	int 	i;
	int		j;

	i = 0;
	while (tetr[i] != '#')
		i++;
	ar[0] = i % 5;
	ar[1] = i / 5;
	j = 2;
	i++;
	while (i++ < 21)
	{
		if (tetr[i] == '#')
		{
			ar[j] = i % 5 - ar[0];
			ar[j + 1] = i / 5 - ar[1];
			j += 2;
		}
	}
	ar[0] = 0;
	ar[1] = 0;
	return (0);
}